public class tugas8_String {

    public static void main(String[] args) {
        
        String kata1 = "HUMMASOFT TECHNOLOGY MALANG";
        String kata2 = "kelas industri bergengsi di indonesia";
        String kata3 = "hummasoft";
        String kata4 = "mempunyai produk";
        String kata5 = "sistem sekolah";

        System.out.println("1. "+kata1.toLowerCase());
        System.out.println("2. "+kata2.toUpperCase());
        System.out.print("3. "+kata3.toUpperCase());
        System.out.print(" "+kata4.toLowerCase());
        System.out.println(" "+kata5.toUpperCase());
    }    
}
